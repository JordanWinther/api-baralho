package com.api.baralho.domain;

public class Image {
	
	protected String svg;
	protected String png;
	
	public String getSvg() {
		return svg;
	}
	public void setSvg(String svg) {
		this.svg = svg;
	}
	public String getPng() {
		return png;
	}
	public void setPng(String png) {
		this.png = png;
	}
	
	

}
